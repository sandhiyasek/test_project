package gov.shdrc.util;

public class Promotion {
		private Long promotionId=null;
		private String promotionType=null;
		private String promotion=null;
		private String promotionDOJ=null;
		public Long getPromotionId() {
			return promotionId;
		}
		public void setPromotionId(Long promotionId) {
			this.promotionId = promotionId;
		}
		public String getPromotionType() {
			return promotionType;
		}
		public void setPromotionType(String promotionType) {
			this.promotionType = promotionType;
		}
		public String getPromotion() {
			return promotion;
		}
		public void setPromotion(String promotion) {
			this.promotion = promotion;
		}
		public String getPromotionDOJ() {
			return promotionDOJ;
		}
		public void setPromotionDOJ(String promotionDOJ) {
			this.promotionDOJ = promotionDOJ;
		}

	
}
